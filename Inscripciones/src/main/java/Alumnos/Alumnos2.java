    private void BtnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnImprimirActionPerformed
        String Nombre=TxtNombre.getText();
        String ApellidoP=TxtApellidoP.getText();
        String ApellidoM=TxtApellidoM.getText();
        String Dia=SpDia.getValue().toString();
        String Mes=SpMes.getValue().toString();
        String Ano=SpAno.getValue().toString();
        String Sexo=null;
        String LNacimiento=JCbNacimiento.getSelectedItem().toString();
        
        if(RbtFemenino.isSelected()){
            Sexo="Femenino";
        }else if(RbtMasculino.isSelected()){
            Sexo="Masculino";
        }
        JOptionPane.showMessageDialog(null, "Nombre completo: "+Nombre+" "+ApellidoP+" "+ApellidoM+"\n"+
                "Fecha de nacimiento: "+Dia+"/"+Mes+"/"+Ano+"\n"+
                "Sexo: "+Sexo+"\n"+
                "Lugar de Nacimiento: "+LNacimiento);
    }//GEN-LAST:event_BtnImprimirActionPerformed

