    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Nombre=TxtNombre.getText();
        String Correo=TxtCorreo.getText();
        String Lugar=CBLugar.getSelectedItem().toString();
        String Dia=SpDia.getValue().toString();
        String Mes=SpMes.getValue().toString();
        String Ano=SpAno.getValue().toString();
        String Tipo=null;
        int Pasajero = (int) SpPasajero.getValue();
        int Precio=0;
        int PrecioTotal=0;
        
        if(RbtRedondo.isSelected() && RbtAereo.isSelected()){
            Tipo="Redondo/Aereo";
            if(Lugar=="Mazatlan")
                Precio=2950;
            if(Lugar=="Culiacan")
                Precio=2300;
            if(Lugar=="Mochis")
                Precio=2800;
        }else if(RbtRedondo.isSelected() && RbtTerrestre.isSelected()){
            Tipo="Redondo/Terrestre";
            if(Lugar=="Mazatlan")
                Precio=2500;
            if(Lugar=="Culiacan")
                Precio=2300;
            if(Lugar=="Mochis")
                Precio=2100;
        }else if(RbtSencillo.isSelected() && RbtAereo.isSelected()){
            Tipo="Sencillo/Aereo";
            if(Lugar=="Mazatlan")
                Precio=1450;
            if(Lugar=="Culiacan")
                Precio=1500;
            if(Lugar=="Mochis")
                Precio=1750;
        }else if(RbtSencillo.isSelected() && RbtTerrestre.isSelected()){
            Tipo="Sencillo/Terrestre";
            if(Lugar=="Mazatlan")
                Precio=640;
            if(Lugar=="Culiacan")
                Precio=750;
            if(Lugar=="Mochis")
                Precio=860;
        }
        
        PrecioTotal=Precio*Pasajero;
        
        JOptionPane.showMessageDialog(null, "Nombre: "+Nombre+"\n"
                +"Correo: "+Correo+"\n"
                +"Lugar de viaje: "+Lugar+"\n"+
                "Fecha del viaje: "+Dia+"/"+Mes+"/"+Ano+"\n"+
                "Numero de pasajeros: "+Pasajero+"\n"+
                "Tipo de viaje: "+Tipo+"\n"+
                "Precio unitario: "+Precio+"\n"+
                "Precio total: "+PrecioTotal);
    }//GEN-LAST:event_jButton1ActionPerformed

